let x = 10;
let y = 3;

console.log(x + y); // addition
console.log(x - y); // subtraction
console.log(x * y); // multiplication
console.log(x / y); // division
console.log(x % y); // remainder of devision
console.log(x ** y); // exponentiation, x to the power of y

// Increment (++)
console.log(x, ++x, x++, x);

// Decrement (--)
console.log(x, --x, x--, x);
